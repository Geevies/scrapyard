package pracexercise;

public class Privileges {
	private char group;
	private int duration;
	private int visitsMonth;

	public Privileges(char g, int d, int vm) {
		this.group = group;
		this.duration = duration;
		this.visitsMonth = visitsMonth;
	}

	public char getGroup() {
		return group;
	}

	public int getDuration() {
		return duration;
	}

	public int getVisitsMonth() {
		return visitsMonth;
	}

}
